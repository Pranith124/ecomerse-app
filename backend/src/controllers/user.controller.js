import {User} from '../models/user.model.js';


export async function addAddress(req,res){
    try{
        const {label,fullName,streetAddress,city,state,zipcode,phoneNumber,isDefault}=req.body;

        const user=req.user;

        if (isDefault){
            User.addresses.forEach((addres)=>{
                addres.isDefault=false;
            })
        }
        user.addresses.push({
            label,
            fullName,
            streetAddress,
            city,
            state,
            zipcode,
            phoneNumber,
            isDefault: isDefault || false,
        })

        await user.save();

        res.status(201).json({message:"Address added successfully",addresses:user.addresses});

    }catch(error){
        console.error("Error in addAddress controller",error);
        res.status(500).json({message:"Internal server error"});
    }

}

export async function getAddress(req,res){
    try{
        const user=req.user;
        res.status(200).json({addresses:user.addresses});
    }catch(error){
        console.error("Error in getAddress controller",error);
        res.status(500).json({message:"Internal server error"});
    }
}

export async function updateAddress(req,res){
    try{
        const {label,fullName,streetAddress,city,state,zipcode,phoneNumber,isDefault}=req.body;
        const {addressId}=req.params;

        const user=req.user;
        const address=user.addresses.id(addressId);

        if(!address){
            return res.status(404).json({message:"Address not found"});
        }

        if (isDefault){
            User.addresses.forEach((addres)=>{
                addres.isDefault=false;
            })
        }
        
        address.label=label || address.label;
        address.fullName=fullName || address.fullName;
        address.streetAddress=streetAddress || address.streetAddress;
        address.city=city || address.city;
        address.state =state || address.state;
        address.zipcode=zipcode || address.zipcode;
        address.phoneNumber=phoneNumber || address.phoneNumber;
        address.isDefault=isDefault !== undefined ? isDefault : address.isDefault;

        await user.save().json({message:"Address updated successfully",addresses:user.addresses});
    }catch(error){
        console.error("Error in updateAddress controller",error);
        res.status(500).json({message:"Internal server error"});
    }
}

export async function deleteAddress(req,res){
    try{
        const {addressId}=req.params;
        const user=req.user;

        await user.addresses.pull(addressId);
        await user.save();
        res.status(200).json({message:"Address deleted successfully",addresses:user.addresses});

    }catch(error){
        console.error("Error in deleteAddress controller",error);
        res.status(500).json({message:"Internal server error"});
    }
}

export async function addWishlist(req,res){
    try{
        const {productId}=req.body;
        const user=req.user;

        if(user.wishlist.includes(productId)){
            return res.status(400).json({error:"Product already in wishlist"});
        }

        user.wishlist.push(productId);
        await user.save();
        res.status(201).json({message:"Product added to wishlist",wishlist:user.wishlist});
    }catch(error){
        console.error("Error in addWishlist controller",error);
        res.status(500).json({message:"Internal server error"});
    }
}

export async function removeFromWishlist(req,res){
    try{
        const {productId}=req.params;
        const user=req.user;

        if(!user.wishlist.includes(productId)){
            return res.status(400).json({error:"Product is not in wishlist"});
        }

        user.wishlist.pull(productId);
        await user.save();

        res.status(200).json({message:"Product removed from wishlist",wishlist:user.wishlist});
    }catch(error){
        console.error("Error in removeFromWishlist controller",error);
        res.status(500).json({message:"Internal server error"});
    }
}

